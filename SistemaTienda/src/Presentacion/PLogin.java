    private void btningresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btningresarActionPerformed
        
       if(TxtUsuario.getText().trim().length() == 0){
            JOptionPane.showMessageDialog(this, "Ingrese Usuario");
            return;
        }
        else if(jpfContraseña.getText().trim().length() == 0){
            JOptionPane.showMessageDialog(this, "Ingrese Contraseña");
            return;         

        }
       Connection cnn =  Conexion.Conexion();
        
        try {
           CallableStatement cst = cnn.prepareCall("{call SP_GET_UsuarioLogin()}");
           cst.execute();
           ResultSet rs = cst.getResultSet();
           while (rs.next()) {  
               System.out.println(rs.getString(3) + " " + rs.getString(4));
                if(rs.getString(3).toString().equals(TxtUsuario.getText()) && rs.getString(4).toString().equals(jpfContraseña.getText())){
                     this.setVisible(false);
                    new PInicio().show();
                    
                    return;
                }
            } 
            JOptionPane.showMessageDialog(this, "Usuario o Contraseña Incorrecta");
       } catch (SQLException ex) {
           
       }
     
      
    }//GEN-LAST:event_btningresarActionPerformed

