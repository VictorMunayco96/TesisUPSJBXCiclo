     private String Guardar(){
         
        String Result = "";
      //  CREACION DE OBJETO DE LA CLASE EMPLEADO
        instDUsuUsuario.setUsuario(TxtUsuario.getText());
        instDUsuUsuario.setContraseña(TxtContraseña.getText().toString());
        instDUsuUsuario.setTipoUsuario(jcbTipoUsuario.getSelectedItem().toString());
        instDUsuUsuario.setEstado(jcbEstado.getSelectedItem().toString());   
        Result =  new NUsuUsuario().SET_Usuario(instDUsuUsuario);
        if(Result.equals("Success") == true ) Nuevo();
        return !Result.equals("Success") ? Result : "Datos Guardados correctamente";
    }
    private void ValidaDatos(){
        
        if(TxtUsuario.getText().trim().length() == 0){
            JOptionPane.showMessageDialog(this, "Ingrese Usuario");
            return;
        }
        else if(TxtContraseña.getText().trim().length() == 0){
            JOptionPane.showMessageDialog(this, "Ingrese Contraseña");
            return;
        }
        else if(jcbTipoUsuario.getSelectedItem().toString().trim().length() == 0){
            JOptionPane.showMessageDialog(this, "Seleccione Tipo Usuario");
            return;
        }
        else if(jcbEstado.getSelectedItem().toString().trim().length() == 0){
            JOptionPane.showMessageDialog(this, "Seleccione Estado");
            return;
        }
    }
