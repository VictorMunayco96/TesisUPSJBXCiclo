    private void TblUsuarioMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TblUsuarioMousePressed
  try {
            if (evt.getClickCount() == 2) {
                instDUsuUsuario.setOpcion("U");
                TxtNombres.setEnabled(false);
                
                int fila = TblUsuario.getSelectedRow();
                instDUsuUsuario.setIdUsuario(Integer.parseInt(TblUsuario.getValueAt(fila, 0).toString()));
                TxtDNI.setText(TblUsuario.getValueAt(fila, 6).toString());
                TxtNombres.setText(TblUsuario.getValueAt(fila, 7).toString());
                TxtUsuario.setText(TblUsuario.getValueAt(fila, 2).toString());
                TxtContraseña.setText(TblUsuario.getValueAt(fila, 3).toString());
                TxtContraseña2.setText(TblUsuario.getValueAt(fila, 3).toString());
                jcbTipoUsuario.setSelectedItem(TblUsuario.getValueAt(fila, 4).toString());
                jcbEstado.setSelectedItem(TblUsuario.getValueAt(fila, 5).toString());
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_TblUsuarioMousePressed

