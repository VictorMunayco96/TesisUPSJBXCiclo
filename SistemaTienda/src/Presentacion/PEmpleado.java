    private String Guardar(){
        String Result = "";
        // CREACION DE OBJETO DE LA CLASE EMPLEADO
        if(!instDDatEmpleado.getOpcion().equals("D")){
        instDDatEmpleado.setDNI(Integer.parseInt(TxtDni.getText()));
        instDDatEmpleado.setSucursal(DOcassionLocal.instDDatSucurrsal.getIdSucursal());
        instDDatEmpleado.setCargo(jcbCargo.getSelectedItem().toString());
        instDDatEmpleado.setSueldo(Double.parseDouble(TxtSueldo.getText()));
        instDDatEmpleado.setEstado(jcbEstado.getSelectedItem().toString());
        }
        Result =  new NDatEmpleado().SET_Empleado(instDDatEmpleado);
        if(Result.equals("Success") == true ) Nuevo();
        CargarDatos(0);
        return !Result.equals("Success") ? Result : "Datos Guardados correctamente";
        
    }
   private void ValidaDatos(){
        
        if(Validaciones.tryParse(TxtDni.getText()) == 0){
            JOptionPane.showMessageDialog(this, "Ingrese nro. DNI valido");
            return;
        }
        else if(TxtDni.getText().trim().length() == 0){
            JOptionPane.showMessageDialog(this, "Ingrese nro. DNI");
            return;
        }
        else if(TxtSucursal.getText().trim().length() == 0){
            JOptionPane.showMessageDialog(this, "Ingrese sucursal");
            return;
        }
        else if(jcbCargo.getSelectedItem()== null){
            JOptionPane.showMessageDialog(this, "Ingrese cargo");
            return;
        }
        else if(Double.parseDouble(TxtSueldo.getText()) == 0){
            JOptionPane.showMessageDialog(this, "Ingrese sueldo");
            return;
        }
        else if(jcbEstado.getSelectedItem()== null){
            JOptionPane.showMessageDialog(this, "Ingrese estado");
            return;
        }
 
    }
