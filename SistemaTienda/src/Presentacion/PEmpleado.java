    private void TblEmpleadoMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TblEmpleadoMousePressed
  
        try {
            if (evt.getClickCount() == 2) {
                instDDatEmpleado.setOpcion("U");
                TxtDni.setEnabled(false);
                
                int fila = TblEmpleado.getSelectedRow();
                instDDatEmpleado.setIdEmpleado(Integer.parseInt(TblEmpleado.getValueAt(fila, 0).toString()));
                TxtDni.setText(TblEmpleado.getValueAt(fila, 1).toString());
                TxtNombre.setText(TblEmpleado.getValueAt(fila, 2).toString());
                jcbCargo.setSelectedItem(TblEmpleado.getValueAt(fila, 3).toString());
                TxtSueldo.setText(TblEmpleado.getValueAt(fila, 4).toString());
                jcbEstado.setSelectedItem(TblEmpleado.getValueAt(fila, 5).toString());
                TxtSucursal.setText(TblEmpleado.getValueAt(fila, 6).toString());
                DOcassionLocal.instDDatSucurrsal = new DDatSucursal();
                DOcassionLocal.instDDatSucurrsal.setIdSucursal(Integer.parseInt((TblEmpleado.getValueAt(fila, 7).toString())));
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_TblEmpleadoMousePressed

